        <h2 class="text-center">Qui sommes-nous ?</h2>
        <p>United Suite, est un conglomérat de plusieurs petites banques qui ont décidé de s'unir lors de la crise
            bancaire de mai 1873.</p>
        <p>Fondée en juillet 1872, la Banque United Suite n'a cessé, au cours des nonantes années qui ont suivi,
            d'acquérir des actionnaires, de racheter ou s'unir avec d'autres banques implantées dans les principales
            villes belges.</p><br>
        <p>En 1931, United Suite s'engagea non seulement dans les activités bancaires traditionnelles, mais également
            dans la gestion d'intérêts industriels principalement localisés en Belgique et en Afrique.</p>
        <p>Suite aux réformes bancaires intervenues en Belgique en 1934 et 1935, l'opération consacrait la séparation
            des activités bancaires et des intérêts de la société de portefeuille.</p>
        <p>La Banque United Suite trouve son origine dans la réunion de trois entreprises bancaires principales, créé
            par les familles Hendrickx-Deltour-El Jaidi, actives dans le secteur bancaire depuis l'indépendance de la
            Belgique, en 1830.</p><br>
        <p>Après 1945, la Banque a développé rapidement ses activités, notamment par le biais de rachats d'autres
            banques.</p>
        <p>La United Suite réunie en assemblée générale extraordinaire le 17 avril 2003, ses actionnaires ont modifié sa
            raison sociale en United Suite Belgique SA.</p>
